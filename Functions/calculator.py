"""Write a python code to create functions for each mathematical operation"""

def addition(a:int,b:int): #parameters
  result = a + b
  return result
def subtraction(a:int,b:int):
  result = a - b
  return result
def multiplication(a:int,b:int):
  result = a*b
  return result
def division(a:int,b:int):
  result = a/b
  return result

a = addition(10,7)
print("Addition =",a)
b = subtraction(10,7)
print("Subtraction =", b)
c = multiplication(10,7)
print("Multiplication =", c)
d = division(10,5)
